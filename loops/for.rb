Em Ruby, as estruturas de repetição mais comuns são for, while, until, each e times. Vou explicar cada uma delas:

1. for:
O loop for é usado para iterar sobre uma coleção de elementos, como um array.



for item in collection
  # código a ser executado para cada item na coleção
end
2. while:
O loop while executa um bloco de código enquanto uma condição específica é verdadeira.



while condition
  # código a ser executado enquanto a condição for verdadeira
end
3. until:
O loop until executa um bloco de código enquanto uma condição específica é falsa.



until condition
  # código a ser executado enquanto a condição for falsa
end
4. each:
O método each é usado para iterar sobre elementos de uma coleção (como um array) usando um bloco.


collection.each do |item|
  # código a ser executado para cada item na coleção
end
5. times:
O método times é usado para iterar um determinado número de vezes.


n.times do
  # código a ser executado n vezes
end
Estas são as estruturas de repetição mais comuns em Ruby. A escolha da estrutura depende do contexto e dos requisitos específicos do seu código.




